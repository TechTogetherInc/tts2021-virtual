function fillData(data){
    document.getElementById('day').innerHTML = `
        ${data.map(sched_template).join('')}
        <h4 style="font-size: 0.9rem;">Check back soon for updates.
		<br>Please note that all times are in Pacific Time (GMT+8).
			<br>Click on any day to see the day's schedule.</h4>
		
    `
}

function sched_template(day_data){
    return `
    <div class="event">
        <h4 class="fs-4 text-primary">
            <span style="font-weight: bold;"> 
				${day_data.Link ? `<a href=${day_data.Link}> ${day_data.Title} </a>` : `${day_data.Title}` }
			</span>
            <span class="fs-6" style="font-weight: bold; float:right">${day_data.Time}</span> 
            <br>
            <span class="fs-6" style="color: #888;"> Hosted by ${day_data.Host} </span>
        </h4>
        <p>${day_data.Description}</p>
    </div>
    `
}

